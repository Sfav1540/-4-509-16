a = {10, 20, 30, 40, 50, 60}
b = {30, 40, 50, 60, 70, 80}
union_result = a | b
intersection_result = a & b
difference_result = a - b
symmetric_difference_result = a ^ b
print(f"ผลรวม (Union): {union_result}")
print(f"ผลส่วนร่วม (Intersection): {intersection_result}")
print(f"ผลต่าง (Difference): {difference_result}")
print(f"ผลต่างสมมาตร (Symmetric Difference): {symmetric_difference_result}")
